import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    glass?: boolean
  }
>(({ className, glass = false, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      glass
        ? "bg-white/70 dark:bg-apple-gray-900/70 backdrop-blur-apple border-apple-gray-200/50 dark:border-apple-gray-800/50"
        : "bg-white dark:bg-apple-gray-900 border-apple-gray-200 dark:border-apple-gray-800",
      "rounded-apple border shadow-apple transition-all duration-300 ease-out",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

